#include <iostream>
//FUNCIONES TRIGONOMETICAS

float seno(float co, float h){
 return co/h;
}

float coseno(float ca, float h){
    return ca/h;
}

float tangente(float co, float ca){
    return co/ca;
}

//Cosecante
float cos(float co, float h){
    return h/co;
}

//Secante
float sec(float ca, float h){
    return h/ca;
}

//cotangente
float cot(float ca, float co){
    return ca/co;
}